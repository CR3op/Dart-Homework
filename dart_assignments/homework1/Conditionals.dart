void main(){
  var x = 3;
  var y = 0;
  
  if(x % 2 == 0){ //check if x is even
    print("x is even");
  }
  else{
    print("x is odd");
  }
  
  if(y < 0){ //check if y is less than zero 
    print("y is less than zero");
  }
  else if(y > 0){ //check if y is greater than zero
    print("y is greater than zero");
  }
  else{
    print("y is equal to zero");
  }
}
