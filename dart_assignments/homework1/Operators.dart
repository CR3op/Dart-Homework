void main() {
    var x = 3;
    var y = 2;

    // NOTE: / (division) operator result is a double
    // NOTE: ~/ (integer division) operator result is a integer
  
    
    print(x - y); //subtract x by y
    print(x + y); //add x and y
    print(x/y); // divide x by y
    print(x ~/ y); //divide x by y(returning an integer result)
    print(x * y); // multiply x by y
    print(x % y); //get the remainder of x divided y 
    print(x == y); //does x equal y
    print(x != y ); //does x not equal y 
}
